// Helper function to get all questions for a role
export const getQuestionsForRole = (roleId) => {
  const role = interviewQuestions[roleId];
  if (!role) return [];
  
  const allQuestions = [
    ...role.categories.technical,
    ...role.categories.hr,
    ...role.categories.behavioral
  ];
  
  return allQuestions;
};

// Helper function to get questions by category
export const getQuestionsByCategory = (roleId, category) => {
  const role = interviewQuestions[roleId];
  if (!role || !role.categories[category]) return [];
  
  return role.categories[category];
};

// Helper function to get role title
export const getRoleTitle = (roleId) => {
  const role = interviewQuestions[roleId];
  return role ? role.title : "Unknown Role";
};
